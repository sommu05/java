package day06;

public class LengthinStringandinArray {

	public static void main(String[] args) {
		String a = "Welcome";
		System.out.println(a.length());    //7
		
		String ab[] = {"Welcome","to","java"};
		System.out.println(ab.length);    //3
		
		char c = 'c';
		System.out.println(c);    //c
		//cannot invoke length in char datatype , because it always 1 in length

	}

}
